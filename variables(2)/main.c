#include <stdio.h>

int main()
{
    int a = 2;
    float b = 2.5;
    char c = 'c'; // single inverted commas is applicable for characters
    printf("The value of integer a is - %d", a);
    printf("The value of float b is - %f", b);
    printf("The value of character c is - %c", c);
    return 0;
}